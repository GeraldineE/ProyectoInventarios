    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (txtUsuario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor Digite un Usuario.");
            txtUsuario.requestFocusInWindow();
            return;
        }

        String Clave = new String(txtClave.getPassword());

        if (Clave.equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor Digite una Clave.");
            txtClave.requestFocusInWindow();
            return;
        }

        if (misDatos.ValidarUsuario(txtUsuario.getText(), Clave)) {

            frmVentanaPrincipal miVentana = new frmVentanaPrincipal();
               
            miVentana.setExtendedState(JFrame.MAXIMIZED_BOTH);
            miVentana.Bienvenida(txtUsuario.getText());
            miVentana.Permisos(misDatos.ConsPerfil(txtUsuario.getText()));
            miVentana.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Usuario o Clave Incorrectos.");
            txtUsuario.setText("");
            txtClave.setText("");
            txtUsuario.requestFocusInWindow();
        }






    }//GEN-LAST:event_jButton1ActionPerformed

